n = int(input())
cnt = 0
total = 0
while True:
  # cnt를 더하다보면 total이 n을 넘을텐데 최대갯수라 cnt-1만 출력하면됨.
  cnt += 1
  total += cnt
  if total > n:
    break

print(cnt - 1)


